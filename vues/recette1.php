<h2 id="titre_Recette">Entrée fraicheur au tartare </h2>
<img id="photo_Recette" src="static/img/photosRecettes/EntreeFraicheurAuTartare.jpg" />

<section id="ingredients">

<h3 id="titre_Ingredients"> Ingrédients </h3>

<ul id="liste_Ingredients">
	<li> concombre(s) : 1 </li>
	<li> ciboulette : 1 botte </li>
	<li> tomate(s) : 6 </li>
	<li> Tartare : 250 g </li>
</ul>

</section>


<section id="preparation">

<h3 id="titre_Preparation">Préparation </h3>

<p class="paragraphe_Ingredients"> Lavez le concombre sans le peler, taillez-le en tranches de 5 mm d’épaisseur environ, bien régulières.</p>

<p class="paragraphe_Ingredients">Lavez les tomates et taillez-les en tranches de la même épaisseur, en supprimant les extrémités. </p>

 

<p class="paragraphe_Ingredients">Tartinez de Tartare les tranches de concombre et de tomates, montez-les en millefeuilles de 3 à 4 couches, 
en commençant systématiquement par des tranches de concombre.</p>

 

<p class="paragraphe_Ingredients">Sur la dernière tranche de légume de chaque bouchée, déposez une petite touche de Tartare avec la pointe d’un couteau,
 et collez-y un brin de ciboulette frais. </p>

 <p class="paragraphe_Ingredients">Réservez au réfrigérateur jusqu’au moment de servir.</p>

</section>


