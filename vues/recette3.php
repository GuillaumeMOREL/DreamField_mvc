<h2 id="titre_Recette">Tarte aux Pommes rapide</h2>

<img id="photo_Recette" src="static/img/photosRecettes/TarteAuxPommes.jpg" />

<section id="ingredients">
<h3 id="titre_Ingredients"> Ingrédients </h3>

<ul id="liste_Ingredients">
	<li> pommes jaunes : 6 </li>
	<li> 1 pâte feuilletée toute prête : 230 g </li>
	<li> sucre en poudre : 60g </li>
	<li> quelques noisettes de beurre</li>
	<li> cannelle en poudre </li>
</ul>

</section>

<section id="preparation">

<h3 id="titre_Preparation">Préparation </h3>

<p class="paragraphe_Ingredients"> Préchauffez le four th.6(180°C).</p>

<p class="paragraphe_Ingredients">Epluchez et coupez les pommes en tranches dans une assiette creuse.</p>

<p class="paragraphe_Ingredients"> Etalez la pâte dans un plat à tarte,
 tout en conservant le papier qui l'entoure (arrondissez avec les ciseaux). </p>

<p class="paragraphe_Ingredients"> Disposez les tranches de pommes sur la pâte, 
	saupoudrez de sucre en poudre et déposez les noisettes de beurre, ça et là, sur les pommes.</p>

<p class="paragraphe_Ingredients"> Saupoudrez de cannelle.</p>


<p class="paragraphe_Ingredients">Enfournez la tarte pendant 15 min.</p>


<p class="paragraphe_Ingredients">Au bout de ce temps, laissez reposer 10 min et coupez en 4 selon les appétits.</p>

</section>


