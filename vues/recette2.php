<h2 id="titre_Recette">Champignons farcis végétariens </h2>

<img id="photo_Recette" src="static/img/photosRecettes/ChampignonFarcisVegetarien.jpg" />

<section id="ingredients">
<h3 id="titre_Ingredients"> Ingrédients </h3>

<ul id="liste_Ingredients">
	<li> gros champignons de Paris : 12 </li>
	<li> beurre ramolli : 80 g </li>
	<li> grosses échalotes : 3 </li>
	<li> gousses d'ail : 2 </li>
	<li> persil frais </li>
	<li> sel, poivre </li>
	<li> parmesan rapé </li>
</ul>

</section>

<section id="preparation">

<h3 id="titre_Preparation">Préparation </h3>

<p class="paragraphe_Ingredients"> Préchauffer le four à 200°C (thermostat 6-7).</p>

<p class="paragraphe_Ingredients">Nettoyer les champignons et les brosser légèrement pour enlever la terre.</p>

<p class="paragraphe_Ingredients">Enlever les queues et couper les bouts terreux. </p>


<p class="paragraphe_Ingredients">Emincer les échalotes et les gousses d'ail.</p>

<p class="paragraphe_Ingredients">Passer les queues de champignons, l'ail et les échalotes, le persil frais ainsi que le beurre ramolli (par exemple au micro-onde mode "décongèlation") au mixer.</p>

<p class="paragraphe_Ingredients">Garnir un plat avec les têtes des champignons.</p>


<p class="paragraphe_Ingredients">Farcir les têtes des champignons avec cette préparation, soupoudrer de parmesan.

<p class="paragraphe_Ingredients">Faire cuire au four 20 minutes environ.</p>

</section>



